    private void botonAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAtrasActionPerformed
        // TODO add your handling code here:
        
        if (botonTimer.isSelected())
        {
            if (ManejoInfo.getTimer().getHoras() != -1 && ManejoInfo.getTimer().getMinutos() != -1 && ManejoInfo.getTimer().getSegundos() != -1)
            {
                Menu menu = new Menu();
                menu.setVisible(true);
                menu.setLocationRelativeTo(null);
                this.setVisible(false);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "No es posible salir del panel "
                        + "ya que se deben validar los datos del timer.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        else
        {
            Menu menu = new Menu();
            menu.setVisible(true);
            menu.setLocationRelativeTo(null);
            this.setVisible(false);
            this.dispose();
        }
    }//GEN-LAST:event_botonAtrasActionPerformed

